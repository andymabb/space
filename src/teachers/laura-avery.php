<p>Laura originally trained as a dancer and first discovered yoga in 2006. She went on to do her initial <abbr title="Registered Yoga Teacher">RYT</abbr>-200 Yoga Alliance accredited teacher training with the Byron Yoga Centre in 2011 and later completed her pre and postnatal teacher training with Bliss Baby Yoga in Australia also.</p>
<p>She went on to teach in Australia, before moving back to the UK, teaching in London and now Thame. Since having children she has also gone on to qualify in teaching children's yoga with Cosmic Kids and loves exploring adventures through yoga with little yogis.</p>
